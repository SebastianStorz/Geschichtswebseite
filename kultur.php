                    <h2>Literatur und Theater <span class="phonetic"><a href="./footer/quellen.php#t7">/Literatúr/</a></span></h2>
                    <p>Thomas Mann, er war einer der bekannten Schriftsteller in der Blütezeit der 20er Jahre. Mit seinem Buch «Der Zauberberg» erlangte er Bekanntheit und das Buch wurde zu einem Klassiker dieser Zeit. Hermann Hesse war auch einer der bekannten Schriftsteller. Mit «Der Steppwolf» erlangt er sein Aufsehen. Sozialreportagen wie «Rasendem Reporter», «Krieg» und «Im Westen nichts Neues» boten gesellschaftskritische Unterhaltung. Die Umstände erlaubten es auch schreibenden Frauen sich zu entwickeln. Das Romanische Cafe gegenüber der Kaiser-Wilhelm-Gedächtniskirche war ein zentraler Treffpunkt für Künstler, weshalb Berlin eine grosse Anziehungskraft ausübte. Frauen konnten sich wegen der Anonymität von der traditionellen Frau distanzieren.</p>
