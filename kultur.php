                <h2>Musik <span class="phonetic"><a href="./footer/quellen.php#t9">/Musik/</a></span></h2>
                <p>Unterhaltungsschlager und Gesellschaftstänze wechselten und verbreiteten sich schnell, der Grund dafür war das Radio. Das Tanzvergnügen gehörte in dieser Zeit zum Lebensstil. Ein amerikanischer Tanz, der Charleston, wurde zum beliebtesten Modetanz in Deutschland. Für die Verbreitung dieses Tanzes sorget unteranderem auch die «Chocolate Kiddies» mit ihrer Musik von Duke Ellington. Noch vor Josephine Baker traten ebenfalls die «Chocolate Kiddies» als eines der ersten amerikanischen Jazzorchester, im Mai 1925, in Berlin auf. Josephine Baker stand mit der «Charleston Jazzband» auf der Bühne. Sie sorgte für Aufregung, weil sie leicht bekleidet war und einen wilden Tanzstil hatte. In den grossen Städten gerieten die Leute in einen noch nie gekannten hemmungslosen Zustand. All die Vergnügungslustigen fanden sich im Jazz wieder. Berlin wurde nebst Paris und London zur Kulturmetropole und lockte viele Talente aus allen Ecken der Welt an.</p>
