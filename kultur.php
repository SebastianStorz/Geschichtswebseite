                <div class="text">
                    <h2>Kunst <span class="phonetic"><a href="./footer/quellen.php#t6">/Kʊnst/;</a></span></h2>
                    <p>Die Nachkriegsjahre wurden von der avantgardistischen Stilrichtung geprägt. Dies wurde mit Rücksichtlosigkeit durchgesetzt. Die Künstler experimentierten. Menschen wurden als Massen, Maschinen oder sogar als Marionetten dargestellt. Gnadenlos wurde kritisiert, wie es zum Beispiel George Grosz in seiner Bildermappe «Ecce Homo» die Phänomene der Zeit machte. Anderweitig versuchte Heinrich Zille, als Maler, Armut und Hunger zu veranschaulichen. Die Kunst war in vielzähligen Museen und Ausstellung zu bewundern und war auch einer breiten Masse der Öffentlichkeit zugänglich. Oftmals stellte sich ein Künstler, mit seiner Kunst, auf eine Seite einer politischen Bewegung. In der mittleren, eher stabilen Phase, war die Kunst ebenfalls in vollen und ganzen Zügen zu bewundern. Die Künstler versuchten die Wirklichkeit darzustellen. Doch all das konnte man nicht von der Architektur und ihrem Design behaupten. Dort war kühle Nüchternheit angesagt. </p>
