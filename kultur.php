                <div class="text">
                    <h2>Medienwelt <span class="phonetic"><a href="./footer/quellen.php#t8">/Médienwelt/</a></span></h2>
                    <p>Massenmedien prägten obendrein auch die 20er Jahre und entfaltete sich rasant. Der Aufschwung der Printmedien konnte man mit dem der Kions gleichstellen. Ende der 20er Jahre verteilten sich etwa 2 Millionen Kinobesucher auf etwa 5'000 Kinos. Der Stummfilm «Metropolis», ein internationaler Klassiker, wurde von der Universum Film AG (UFA) in Potsdam-Babelsberg produziert. Die Universum Film AG (UFA) entwickelte sich zum zweitgrössten Filmimperium der Welt, nach Hollywood. «Der blaue Engel», der erste grosse deutsche Tonfilm, verhalf Marlene Dietrich zum Durchbruch zu einem Weltstar. Die Zahl der Radios stieg rasant an. Dadurch verbreiteten sich Sachen schnell wie Tänze oder Musikstil.</p>
