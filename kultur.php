                            <h3>Sport</h3>
                        </a>
                        <p>Sport zog neuerdings ein grosses Publikum an. Bei Fussballspielen bewegte es Hundertausende Zuschauer in die Stadien, obwohl es noch im Kaiserreich als «undeutsche Fusslümmelei» galt. Bei Rad- und Autorennen zog ebenso viele Zuschauer an wie bei Boxkämpfen. Die Nachfrage nach Sportveranstaltungen war riesig. Jedoch gab es noch kein Fernseher und die Leute verfolgten die Veranstaltungen vor Ort oder sie hörten über Radio zu, wie es bei den Kämpfen von Max Schmeling war, denn während seinen Kämpfen verfolgten Millionen von Hörer den Kampf über das Radio. In nur kurzer Zeit war die Anzahl der Rundfunkgeräte von 10.000 auf 5,4 Millionen gestiegen. </p>
