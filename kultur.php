                <div class="text">
                    <h2>Unterhaltung <span class="phonetic"><a href="./footer/quellen.php#t5">/Unterháltung/</a></span></h2>
                    <p>Frei von jeglicher Zensur, erlebt die Kunst und Kultur während der nicht optimalen politischen Lage einen schnellen Aufschwung. In den 1920er übten die Künstler mit ihren glanzvollen, neuen und speziellen Kunstwerken Kritik an ihrer momentanen gesellschaftlichen Lage aus. Nach amerikanischem Vorbild entfaltete sich die die Massenkultur. Erstmals zog es viele Leute ins Kino und bei Sportveranstaltungen versammelte sich ein grosses Publikum. Zum Lebensstil der «Goldenen Zwanziger» gehört sich Musik und Tanzvergnügen. Aufbruchstimmung und kulturelle Experimentierfreudigkeit, für aussergewöhnliche Partys und nie endender Vergnügungssucht, für Verruchtheit und sexuelle Freizügigkeit, für all dies stehen diese Jahre, dennoch war das nicht bei allen so. Nur wenige Reiche konnten sich so einen Lebensstil leisten. Die bürgerliche Kultur lies dieser Trend kalt, denn sie führten unbeeindruckt ihr Leben so weiter, wie es zuvor war.</p>
